#===================================================================================================================#
#                           for loop with 3 argument                                                                #
#===================================================================================================================#

for i in range(1,50,3):  #start : stop(-1):step
    print('hello',i)

#===================================================================================================================#
#                               for loop with three argument (Down loop)                                            #
#===================================================================================================================#

for x in range(10,1,-1):
    print("Rakesh",x)